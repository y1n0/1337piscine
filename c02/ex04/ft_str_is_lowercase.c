#include <stdio.h>

int	ft_str_is_lowercase(char *str)
{
	char	c;

	while (*str != '\0')
	{
		c = *str;
		if (!('a' <= c && c <= 'z'))
			return (0);
		str++;
	}
	return (1);
}
